package Class18;

public class Cat {

    String name;
    String breed;
    int age;
    String color;
    boolean dangerous;

    public Cat(String name, String breed, int age, String color, boolean dangerous) {
        this.name = name;
        this.breed = breed;
        this.age = age;
        this.color = color;
        this.dangerous = dangerous;
    }
}
