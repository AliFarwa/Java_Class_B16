package Class7;

public class E2WhileLoops {
    public static void main(String[] args) {
        int number=0;
        number++;// 0+ 1=1 this ++ will add 1
        number++;//1+2
        System.out.println(number);
        int number2=2;
        number2--;
        System.out.println(number2);
    }
}
